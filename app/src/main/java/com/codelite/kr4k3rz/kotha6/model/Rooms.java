package com.codelite.kr4k3rz.kotha6.model;


public class Rooms {
    private String fistName;
    private String lastName;
    private String about;
    private String location;
    private String type_of_bed;
    private String no_of_roomMates;
    private String description;

    public Rooms() {
    }

    public String getFistName() {
        return fistName;
    }

    public void setFistName(String fistName) {
        this.fistName = fistName;
    }

    public String getLastName() {

        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getType_of_bed() {
        return type_of_bed;
    }

    public void setType_of_bed(String type_of_bed) {
        this.type_of_bed = type_of_bed;
    }

    public String getNo_of_roomMates() {
        return no_of_roomMates;
    }

    public void setNo_of_roomMates(String no_of_roomMates) {
        this.no_of_roomMates = no_of_roomMates;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
